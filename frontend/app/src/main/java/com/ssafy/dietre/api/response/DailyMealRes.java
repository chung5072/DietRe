package com.ssafy.dietre.api.response;

public class DailyMealRes {
    private Integer recommendedCalorie;
    private Integer realCalorie;
    private Integer recommendedCarbohydrate;
    private Integer realCarbohydrate;
    private Integer recommendedFat;
    private Integer realFat;
    private Integer recommendedProtein;
    private Integer realProtein;

    public Integer getRecommendedCalorie() {
        return recommendedCalorie;
    }

    public void setRecommendedCalorie(Integer recommendedCalorie) {
        this.recommendedCalorie = recommendedCalorie;
    }

    public Integer getRealCalorie() {
        return realCalorie;
    }

    public void setRealCalorie(Integer realCalorie) {
        this.realCalorie = realCalorie;
    }

    public Integer getRecommendedCarbohydrate() {
        return recommendedCarbohydrate;
    }

    public void setRecommendedCarbohydrate(Integer recommendedCarbohydrate) {
        this.recommendedCarbohydrate = recommendedCarbohydrate;
    }

    public Integer getRealCarbohydrate() {
        return realCarbohydrate;
    }

    public void setRealCarbohydrate(Integer realCarbohydrate) {
        this.realCarbohydrate = realCarbohydrate;
    }

    public Integer getRecommendedFat() {
        return recommendedFat;
    }

    public void setRecommendedFat(Integer recommendedFat) {
        this.recommendedFat = recommendedFat;
    }

    public Integer getRealFat() {
        return realFat;
    }

    public void setRealFat(Integer realFat) {
        this.realFat = realFat;
    }

    public Integer getRecommendedProtein() {
        return recommendedProtein;
    }

    public void setRecommendedProtein(Integer recommendedProtein) {
        this.recommendedProtein = recommendedProtein;
    }

    public Integer getRealProtein() {
        return realProtein;
    }

    public void setRealProtein(Integer realProtein) {
        this.realProtein = realProtein;
    }
}
